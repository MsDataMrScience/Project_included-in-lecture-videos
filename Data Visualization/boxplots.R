#In a boxplot, the box is defined by the 25th and 75th percentiles and the median 
#is a horizontal line through the box. The whiskers show the range excluding outliers, 
#and outliers are plotted separately as individual points.

#The interquartile range is the distance between the 25th and 75th percentiles.

#Boxplots are particularly useful when comparing multiple distributions.

#When data do not follow a normal distribution and cannot be succinctly summarized by 
#only the mean and standard deviation, an alternative is to report a five-number summary:
#range (ignoring outliers) and the quartiles (25th, 50th, 75th percentile)